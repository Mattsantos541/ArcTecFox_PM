import { createClient } from "@supabase/supabase-js";
import axios from "axios";

// üîπ Load Environment Variables Safely (No Hardcoded Keys)
const SUPABASE_URL = import.meta.env.VITE_SUPABASE_URL;
const SUPABASE_ANON_KEY = import.meta.env.VITE_SUPABASE_ANON_KEY;

if (!SUPABASE_URL || !SUPABASE_ANON_KEY) {
  console.error("‚ùå Supabase credentials are missing. Check your .env file!");
}

const supabase = createClient(SUPABASE_URL, SUPABASE_ANON_KEY);

// üîπ Backend API URL (For fetching assets & metrics)
const API_URL = `${window.location.protocol}//${window.location.hostname}:9000`;

/** ======================
 *  üîπ AUTHENTICATION METHODS (Fixed Sign Up)
 *  ====================== */

// ‚úÖ Sign Up User & Insert into public.users
export const signUp = async (email, password, fullName, industry, companySize) => {
  // Step 1: Create user in auth.users (handled by Supabase)
  const { data, error } = await supabase.auth.signUp({ email, password });
  if (error) throw error;

  const user = data?.user; // Get the created user

  // Step 2: Insert into public.users table (if user was created)
  if (user) {
    const { error: insertError } = await supabase
      .from("users") // Make sure this matches your Supabase table name
      .insert([
        {
          id: user.id, // Matches auth.users UUID
          email,
          full_name: fullName,
          industry,
          company_size: companySize,
          created_at: new Date().toISOString(),
        },
      ]);

    if (insertError) {
      console.error("‚ùå Error inserting user into public.users:", insertError.message);
      throw insertError;
    }
  }

  return user;
};

// ‚úÖ Get User Profile from `public.users`
export const getUserProfile = async (userId) => {
  const { data, error } = await supabase
    .from("users")
    .select("*")
    .eq("id", userId)
    .single();

  if (error) {
    console.error("‚ùå Error fetching user profile:", error.message);
    return null;
  }

  return data;
};

// ‚úÖ Sign In User
export const signIn = async (email, password) => {
  const { data, error } = await supabase.auth.signInWithPassword({ email, password });

  if (error) throw error;
  return data.user;
};

// ‚úÖ Sign Out User
export const signOut = async () => {
  const { error } = await supabase.auth.signOut();
  if (error) throw error;
};

// ‚úÖ Get Current User
export const getCurrentUser = async () => {
  const { data, error } = await supabase.auth.getUser();
  if (error) return null;
  return data?.user || null;
};

/** ======================
 *  üîπ DATA FETCHING METHODS
 *  ====================== */

// ‚úÖ Fetch Asset Data
export const fetchAssets = async () => {
  try {
    console.log("üì° Fetching assets from:", `${API_URL}/assets`);
    const response = await axios.get(`${API_URL}/assets`);
    return response.data || [];
  } catch (error) {
    console.error("‚ùå Error fetching assets:", error);
    return [];
  }
};

// ‚úÖ Fetch Company Metrics
export const fetchMetrics = async () => {
  try {
    console.log("üì° Fetching company metrics from:", `${API_URL}/metrics`);
    const response = await axios.get(`${API_URL}/metrics`);
    return response.data || {
      totalAssets: 0,
      activePMPlans: 0,
      nextPMTask: "N/A",
      locations: [],
    };
  } catch (error) {
    console.error("‚ùå Error fetching metrics:", error);
    return {
      totalAssets: 0,
      activePMPlans: 0,
      nextPMTask: "N/A",
      locations: [],
    };
  }
};

export default supabase;
